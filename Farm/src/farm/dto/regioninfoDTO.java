package farm.dto;

public class regioninfoDTO {

	int regionNum;
	int farmNum;
	String regionName;
	String regionSize;
	String regionMaxrentterm;
	int regionPrice;
	int regionRentstate;
	
	public int getRegionNum() {
		return regionNum;
	}
	public void setRegionNum(int regionNum) {
		this.regionNum = regionNum;
	}
	public int getFarmNum() {
		return farmNum;
	}
	public void setFarmNum(int farmNum) {
		this.farmNum = farmNum;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getRegionSize() {
		return regionSize;
	}
	public void setRegionSize(String regionSize) {
		this.regionSize = regionSize;
	}
	public String getRegionMaxrentterm() {
		return regionMaxrentterm;
	}
	public void setRegionMaxrentterm(String regionMaxrentterm) {
		this.regionMaxrentterm = regionMaxrentterm;
	}
	public int getRegionPrice() {
		return regionPrice;
	}
	public void setRegionPrice(int regionPrice) {
		this.regionPrice = regionPrice;
	}
	public int getRegionRentstate() {
		return regionRentstate;
	}
	public void setRegionRentstate(int regionRentstate) {
		this.regionRentstate = regionRentstate;
	}
	
}
